// backend/src/modules/shipmozo/shipmozo.routes.js
const express = require('express');
const router = express.Router();
const { createShipment, trackByAwb, trackOrder, cancelShipment, checkServiceability, handleWebhook } = require('./shipmozo.controller');
const { protect, adminOnly } = require('../../middleware/auth.middleware');

// ?? Webhook (public, called by ShipMozo) ?????????????????????????????????????
// Must use express.json() here explicitly because index.js mounts this router
// after the global parser - we keep it here for clarity and future raw-body needs.
router.post('/webhook', express.json({ type: '*/*' }), handleWebhook);

// ?? Admin endpoints ???????????????????????????????????????????????????????????
router.post('/orders/:orderId/create-shipment', protect, adminOnly, createShipment);
router.post('/orders/:orderId/cancel-shipment', protect, adminOnly, cancelShipment);
router.get('/serviceability', protect, adminOnly, checkServiceability);

// ?? Customer + admin: track by order id (requires login) ?????????????????????
router.get('/orders/:orderId/track', protect, trackOrder);

// ?? Public: track by AWB number directly ?????????????????????????????????????
router.get('/track/:awbNumber', trackByAwb);

module.exports = router;

